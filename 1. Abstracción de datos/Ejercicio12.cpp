// Clasificación de Estructuras de Datos

// Orientadas hacia los Datos
// Arreglos (Arrays):
// Descripción: Colección de elementos del mismo tipo organizados en una secuencia.
// Uso: Eficiencia en el acceso aleatorio y almacenamiento contiguo en memoria.
// Listas Enlazadas (Linked Lists):
// Descripción: Conjunto de nodos donde cada nodo apunta al siguiente, permitiendo inserciones y eliminaciones eficientes.
// Uso: Cuando se requiere una gestión dinámica de datos con un tamaño variable.
// Árboles (Trees):
// Descripción: Estructura jerárquica donde cada nodo tiene un valor y puede tener nodos hijos.
// Uso: Para representar datos jerárquicos y facilitar búsquedas eficientes.

// Orientadas hacia el Flujo
// Colas (Queues):
// Descripción: Estructura que sigue el principio FIFO (First In, First Out), donde los datos se procesan en el orden en que llegaron.
// Uso: En sistemas de gestión de tareas, procesamiento de datos en tiempo real, y manejo de solicitudes en servidores.
// Pilas (Stacks):
// Descripción: Estructura que sigue el principio LIFO (Last In, First Out), donde los últimos elementos añadidos son los primeros en ser procesados.
// Uso: En algoritmos de retroceso, manejo de funciones y navegación de páginas web.

// Orientadas hacia el Concepto
// Grafos (Graphs):
// Descripción: Conjunto de nodos (vértices) conectados por aristas, que modelizan relaciones.
// Uso: Para representar relaciones en redes sociales, rutas en mapas, y conexiones en sistemas de comunicación.
// Tablas Hash (Hash Tables):
// Descripción: Estructura que asocia claves a valores, utilizando una función hash para acceso rápido.
// Uso: Para implementar diccionarios, donde se requiere búsqueda y almacenamiento eficiente.