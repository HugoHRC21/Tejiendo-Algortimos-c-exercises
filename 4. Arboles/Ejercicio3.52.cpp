// Nodo del √°rbol de Huffman
struct HuffNode {
    char symbol;
    HuffNode* left;
    HuffNode* right;
    HuffNode(char s = '\0') : symbol(s), left(nullptr), right(nullptr) {}
};

// Construye manualmente parte del √°rbol (debes completarlo)
HuffNode* build_huffman_tree() {
    HuffNode* root = new HuffNode();

    // Ejemplo de inserci√≥n de c√≥digos Huffman:
    // A -> 00, B -> 01, etc. (s√≥lo ejemplo)
    auto insert_code = [&](const string& code, char symbol) {
        HuffNode* curr = root;
        for (char bit : code) {
            if (bit == '0') {
                if (!curr->left) curr->left = new HuffNode();
                curr = curr->left;
            } else {
                if (!curr->right) curr->right = new HuffNode();
                curr = curr->right;
            }
        }
        curr->symbol = symbol;
    };

    // Aqu√≠ debes insertar cada c√≥digo (seg√∫n tu √°rbol)
    insert_code("00", 'A');
    insert_code("010", 'B');
    // ... agrega todos los c√≥digos reales

    return root;
}

// Decodificar usando el √°rbol
string decode(HuffNode* root, const string& encoded) {
    string result;
    HuffNode* current = root;
    for (char bit : encoded) {
        if (bit == '0')
            current = current->left;
        else
            current = current->right;

        if (current->left == nullptr && current->right == nullptr) {
            result += current->symbol;
            current = root; // volver a la ra√≠z
        }
    }
    return result;
}

